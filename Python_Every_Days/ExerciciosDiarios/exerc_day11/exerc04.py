'''
Faça um Programa que verifique se uma 
letra digitada é vogal ou consoante.
'''

letra = input("digite uma letra: ")

if  letra == 'a' or letra == 'e' or letra == 'i' or letra == 'o' or letra == 'u':
    print(f'a letra: {letra} é uma vogal.')
elif letra == 'b' or letra == 'c' or letra == 'd' or letra == 'f' or letra == 'g' or letra == 'h' or letra == 'j' or letra == 'k' or letra == 'l' or letra == 'p':
    print(f'a letra: {letra} é uma coansoante.')
elif letra == 'q' or letra == 'r' or letra == 's' or letra == 'v' or letra == 'w' or letra == 'y' or letra == 'z':
    print(f'a letra: {letra} é uma coansoante.')
    