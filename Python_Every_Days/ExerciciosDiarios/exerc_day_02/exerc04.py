

# Faça um Programa que peça as 4 notas bimestrais e mostre a média.

num1 = float(input("Digite o primeiro numero inteiro: "))
num2 = float(input("Digite o segundo numero inteiro: "))
num3 = float(input("Digite o terceiro numero inteiro: "))
num4 = float(input("Digite o quarto numero inteiro: "))

media = ((num1+num2+num3+num4)/4)

print("A media digitada e: ")
print(media)
