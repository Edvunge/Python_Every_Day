'''
Faça um programa que converta a temperatura de Celsius para Fahrenheit. 
Solicite ao usuário que insira a temperatura em Celsius e 
exiba a temperatura equivalente em Fahrenheit.
'''
var_temp_celsius = float(input("Digite a temperatura em Celsius"))

var_temp_fahrenheit = (8 * var_temp_celsius)

print(f'a temperatura em graus celsius eh {var_temp_fahrenheit}.')