""" 
Python = Linguagem de Progamação
Tipo de tipagem = Dinamica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(23+1+1)


# Aspas simples
print('Edvaldo Vunge')
print(1, 'Edvaldo "Vunge"')

# Aspas duplas
print("Edvaldo Vunge")
print(2, "Edvaldo 'Vunge'")

# Escape
print("Edvaldo \"Vunge\"")
