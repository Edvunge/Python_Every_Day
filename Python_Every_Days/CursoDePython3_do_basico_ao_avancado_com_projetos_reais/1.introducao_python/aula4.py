# Aula4 - Tipo int e float - introdução aos tipos de dados
# Tipos int e float 
# int -> numero inteiro
# o tipo int representa qualquer numero
# positivo ou negativo. int sem sinal é considerado
# positivo.

print(11) # int 
print(-11) # int 
print(0) 

# float -> numero com ponto flutuante 
# o tipo float representa qualquer numero
# posito ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
print(1.2, 10.11)
print(0.0)

# A função type mostra o tipo que o python 
# inferiu ao valor.
#
#

print(type("edvaldo"))
print(type(23))
print(type(1.2))
