# Aula5 - Tipo bool(boolean) - introdução aos tipos de dados
# Tipo de dado bool (boolean)
# Ao questionar algo em um programa.
# só existem duas respostas possiveis: 
# sim (True) ou não (False).
# Existem varios operadores para "questionar".
# Dentre eles 0 ==, que é um operador lógico que 
# questiona se um valor é igual a outro

print(10 == 10) # sim => true
print(10 == 120) # não => false
print(10 > 102) # não => false
print(10 < 103) # sim => true
print(10 != 10) # não => false
