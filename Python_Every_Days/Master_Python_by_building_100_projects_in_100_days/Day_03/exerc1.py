''' 
1. Faça um programa que leia dois valores numéricos inteiros 
e efetue a adição, caso o resultado seja maior que 10, apresentá-lo.
'''

num1 = int(input("Digite o primeiro valor: "))
num2 = int(input("Digite o segundo valor: "))

